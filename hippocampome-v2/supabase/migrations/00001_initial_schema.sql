-- ============================================================
-- ENUMS
-- ============================================================

CREATE TYPE neuron_status AS ENUM ('active', 'frozen');
CREATE TYPE neurotransmitter_type AS ENUM ('excitatory', 'inhibitory', 'unknown');
CREATE TYPE connection_status AS ENUM ('positive', 'negative', 'unknown');
CREATE TYPE fragment_type AS ENUM ('data', 'protocol', 'animal');
CREATE TYPE error_type AS ENUM ('std', 'sem');
CREATE TYPE evidence_relation_type AS ENUM ('interpretation', 'inference');

-- ============================================================
-- SUBREGION lookup
-- ============================================================

CREATE TABLE subregion (
