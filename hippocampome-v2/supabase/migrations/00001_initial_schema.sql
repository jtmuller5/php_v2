-- ============================================================
-- NEURON TYPE (central entity)
-- ============================================================

CREATE TABLE neuron_type (
  id INT PRIMARY KEY,
  subregion_id TEXT NOT NULL REFERENCES subregion(id),
  name TEXT NOT NULL,
  nickname TEXT NOT NULL,
  status neuron_status NOT NULL DEFAULT 'active',
  excit_inhib neurotransmitter_type NOT NULL DEFAULT 'unknown',
  supertype TEXT,
  type_subtype TEXT,
  position INT,
  ranks TEXT,
  notes TEXT,
  explanatory_notes TEXT,
  v2p0 BOOLEAN NOT NULL DEFAULT false,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  search_vector TSVECTOR GENERATED ALWAYS AS (
    setweight(to_tsvector('english', coalesce(nickname, '')), 'A') ||
    setweight(to_tsvector('english', coalesce(name, '')), 'B') ||
    setweight(to_tsvector('english', coalesce(notes, '')), 'D')
  ) STORED
);

CREATE INDEX idx_neuron_type_search ON neuron_type USING GIN (search_vector);
CREATE INDEX idx_neuron_type_subregion ON neuron_type(subregion_id);
CREATE INDEX idx_neuron_type_status ON neuron_type(status);

-- ============================================================
-- ARTICLES
-- ============================================================

CREATE TABLE article (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  pmid_isbn BIGINT,
  pmcid TEXT,
  nihmsid TEXT,
  doi TEXT,
  open_access BOOLEAN,
  title TEXT,
  publication TEXT,
  volume TEXT,
  issue TEXT,
  first_page INT,
  last_page INT,
  year TEXT,
  citation_count INT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  search_vector TSVECTOR GENERATED ALWAYS AS (
    setweight(to_tsvector('english', coalesce(title, '')), 'A') ||
    setweight(to_tsvector('english', coalesce(publication, '')), 'B')
  ) STORED
);

CREATE INDEX idx_article_search ON article USING GIN (search_vector);
CREATE INDEX idx_article_pmid ON article(pmid_isbn);

-- ============================================================
-- AUTHORS
-- ============================================================

CREATE TABLE author (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL
);

CREATE TABLE article_author (
  article_id INT NOT NULL REFERENCES article(id),
  author_id INT NOT NULL REFERENCES author(id),
  position INT,
  PRIMARY KEY (article_id, author_id)
);

-- ============================================================
-- EVIDENCE
-- ============================================================

CREATE TABLE evidence (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE article_evidence (
  article_id INT NOT NULL REFERENCES article(id),
  evidence_id INT NOT NULL REFERENCES evidence(id),
  PRIMARY KEY (article_id, evidence_id)
);

CREATE TABLE evidence_evidence (
  parent_evidence_id INT NOT NULL REFERENCES evidence(id),
  child_evidence_id INT NOT NULL REFERENCES evidence(id),
  relation_type evidence_relation_type NOT NULL,
  PRIMARY KEY (parent_evidence_id, child_evidence_id)
);

-- ============================================================
-- PROPERTY (subject/predicate/object triples)
-- ============================================================

CREATE TABLE property (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  subject TEXT NOT NULL,
  predicate TEXT NOT NULL,
  object TEXT NOT NULL
);

CREATE INDEX idx_property_subject ON property(subject);
CREATE INDEX idx_property_triple ON property(subject, predicate, object);

-- Key junction table: links evidence + property + neuron type + article
CREATE TABLE evidence_property_type (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  evidence_id INT NOT NULL REFERENCES evidence(id),
  property_id INT NOT NULL REFERENCES property(id),
  type_id INT NOT NULL REFERENCES neuron_type(id),
  article_id INT REFERENCES article(id),
  priority INT,
  unvetted BOOLEAN NOT NULL DEFAULT false,
  linking_quote TEXT,
  interpretation_notes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_ept_type ON evidence_property_type(type_id);
CREATE INDEX idx_ept_property ON evidence_property_type(property_id);
CREATE INDEX idx_ept_evidence ON evidence_property_type(evidence_id);

-- ============================================================
-- FRAGMENT (text quotes from papers)
-- ============================================================

CREATE TABLE fragment (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  original_id INT,
  quote TEXT,
  page_location TEXT,
  fragment_type fragment_type,
  attachment TEXT,
  attachment_type TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  search_vector TSVECTOR GENERATED ALWAYS AS (
    to_tsvector('english', coalesce(quote, ''))
  ) STORED
);

CREATE INDEX idx_fragment_search ON fragment USING GIN (search_vector);

CREATE TABLE evidence_fragment (
  evidence_id INT NOT NULL REFERENCES evidence(id),
  fragment_id INT NOT NULL REFERENCES fragment(id),
  PRIMARY KEY (evidence_id, fragment_id)
);

CREATE TABLE fragment_type_rel (
  fragment_id INT NOT NULL REFERENCES fragment(id),
  type_id INT NOT NULL REFERENCES neuron_type(id),
  priority INT,
  PRIMARY KEY (fragment_id, type_id)
);

-- ============================================================
-- SYNONYM
-- ============================================================

CREATE TABLE synonym (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL,

  search_vector TSVECTOR GENERATED ALWAYS AS (
    to_tsvector('english', name)
  ) STORED
);

CREATE INDEX idx_synonym_search ON synonym USING GIN (search_vector);

CREATE TABLE synonym_type (
  synonym_id INT NOT NULL REFERENCES synonym(id),
  type_id INT NOT NULL REFERENCES neuron_type(id),
  PRIMARY KEY (synonym_id, type_id)
);

CREATE TABLE article_synonym (
  article_id INT NOT NULL REFERENCES article(id),
  synonym_id INT NOT NULL REFERENCES synonym(id),
  PRIMARY KEY (article_id, synonym_id)
);

